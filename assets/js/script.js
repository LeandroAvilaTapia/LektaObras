const { createApp } = Vue;

const app = createApp({
  data() {
    return {
      contadorID: 1,
      imagenes: [],
      currentIndex: 0,
      clientesVue: clientes,
      proyectosVue: proyectos,
    };
  },
  methods: {
    cambiarContadorID: function (id) {
      this.contadorID = id;
      this.currentIndex = 0; // Cuando cambias el ID, reinicia el índice a 0
    },
    cambiarImagen(direccion) {
      // Cambiar la imagen actual basada en la dirección (1 para siguiente, -1 para anterior)
      this.currentIndex =
        (this.currentIndex + direccion + this.imagenes.length) %
        this.imagenes.length;
    },
  },
  created: function () {
    document.documentElement.style.setProperty(
      "--cantidadClientes",
      this.clientesVue.length
    );
  },
  computed: {
    montarImagenes() {
      const proyecto = this.proyectosVue.find((p) => p.Id === this.contadorID);
      if (proyecto) {
        this.imagenes = Object.values(proyecto.Imágenes);
      } else {
        this.imagenes = [];
      }
    },
  },
}).mount("#app");
